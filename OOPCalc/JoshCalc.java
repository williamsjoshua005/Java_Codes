public class JoshCalc {

  public static void main(String args[]) {

    int number1 = Integer.parseInt(args[0]);
    int number2 = Integer.parseInt(args[1]);

    int addition = number1 + number2;
    int multipl = number1 * number2;
    int substract = number1 - number2;
    int division = number1 / number2;

    System.out.println("The Addition of " +number1 + "and " + number2 + " is : " +addition);
    System.out.println("The Multiplication  of " +number1 + "and " + number2 + " is : " +multipl);
    System.out.println("The Substraction of " +number1 + "and " + number2 + " is : " +substract);
    System.out.println("The Division  of " +number1 + "and " + number2 + " is : " +division);



  
  
}

}















